fun Application.configureSecurity() {
    install(CSRF) {
        // tests Origin is an expected value
        allowOrigin("http://localhost:8080")
    
        // tests Origin matches Host header
        originMatchesHost()
    
        // custom header checks
        checkHeader("X-CSRF-Token")
    }
}
